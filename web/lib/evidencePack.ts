// SOC2-style evidence pack builder.
//
// Procurement reality: every enterprise security questionnaire ends with
// "please share evidence that controls X, Y, Z are operating effectively".
// This module produces a single deterministic .zip an admin can hand to
// their auditor (or attach to a vendor questionnaire) without us writing
// a bespoke PDF report each cycle.
//
// What's inside:
//   README.md                       — index + how to interpret each file
//   manifest.json                   — generation metadata + sha256 of each
//                                     file in the pack
//   controls-inventory.json         — every enterprise control, status,
//                                     and the route that owns it
//   policies/                       — current value of every workspace
//                                     policy (SSO, network, CORS, CSP,
//                                     retention, rotation, egress, ...)
//   audit-chain-verification.json   — tamper-evident chain replay result
//   keys.json                       — every API key, scopes/role/revoked
//                                     flag, ip allowlist (no secrets)
//   sessions.json                   — active SSO sessions snapshot
//
// Determinism: every file is JSON.stringify'd with 2-space indent and
// sorted top-level keys. The pack always includes a `generated_at` field
// (non-deterministic by design), so an auditor can prove they're holding
// the pack issued for a specific date. SHA-256 hashes in manifest.json
// let the auditor verify the bundle was not edited after handoff.
//
// Auth: callers must already be admin-gated. This builder does not
// re-check auth; it just gathers data the caller has been authorized to
// read.

